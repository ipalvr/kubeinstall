#systemctl disable firewalld
#firewall-cmd --reload
#systemctl stop firewalld
#sleep 1s
echo "------------------------------------------------------------------------------------------------------------------------------------"
echo "${green}Set bridged packets to traverse iptables rules${reset}"
echo "------------------------------------------------------------------------------------------------------------------------------------"
cat <<EOF | sudo tee /etc/modules-load.d/k8s.conf
